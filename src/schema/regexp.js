const regexp = {
  image: ['image/jpeg', 'image/jpg', 'image/png'],
  password:
    /^.*((?=.*[!@#$%^&*()\-_=+{};:,<.>]){1})(?=.*\d)((?=.*[a-z]){1})((?=.*[A-Z]){1}).*$/,
  mobile: /(\+088)?-?01[0-9]\d{8}/g,
  alphabet: /^[A-Z a-z]+$/,
  email:
    /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  objectId: /^[0-9a-fA-F]{24}$/,
}

module.exports = regexp
